class Roster {

    constructor(name, time, description, owner, discordChannelDestination, discordGuildsToPing, roles) {
        this.name = name;
        this.time = time;
        this.description = description;
        this.owner = owner;
        this.discordChannelDestination = discordChannelDestination;
        this.discordGuildsToPing = discordGuildsToPing;
        this.roles = roles;
    }


}